"""
Tuples are written in () brackets.
Tuples are immutable i.e. can't be changed
"""

tup1 = (1, 3.14, True, "Saksham")
tup2 = (1)  # This is not tuple this in int
tup3 = (1,)  # If we want to make tuple of single element then we should write like this

print(type(tup1))
print("tup1 is " + str(tup1))
print("tup2 is " + str(tup2))
print("type of tup2 is " + str(type(tup2)))
print("tup3 is " + str(tup3))
